let ranNum = Math.floor(Math.random() * (10 - 5 + 1)) + 5;
console.log(ranNum);

switch (ranNum) {
    case 6:
        console.log("Zahl 6");
        break;
    case 7:
        console.log("Zahl 7");
        break;
    case 8:
        console.log("Zahl 8");
        break;
    case 9:
        console.log("Zahl 9");
        break;
    case 10:
        console.log("Zahl 10");
        break;
    default:
        console.log("Zahl nicht zwischen 4 und 10");
        break;

}